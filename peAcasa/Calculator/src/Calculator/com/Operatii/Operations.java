package Calculator.com.Operatii;

public class Operations {
    public void Adunare(int a, int b){
        System.out.print("a + b = "); System.out.println(a+b);
    }
    public void Scadere(int a, int b){
        System.out.print("a - b = "); System.out.println(a-b);
    }
    public void Inmultire(int a, int b){
        System.out.print("a * b = "); System.out.println(a*b);
    }
    public void Impartire(double a, int b){
        System.out.print("a / b = "); System.out.println(a/b);
    }
}
